package com.cg.entity;

public class Student {
	
	public String name;
	public int id;
	public String dept;
	public int rollno;
	
	public Student()
	{
		
	}
	public Student(String name, int id, String dept, int rollno)
	{
		super();
		this.name = name;
		this.id = id;
		this.dept = dept;
		this.rollno = rollno;
	}
	
	
	
	
	

}
